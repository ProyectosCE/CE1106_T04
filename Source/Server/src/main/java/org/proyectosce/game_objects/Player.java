package org.proyectosce.game_objects;

public class Player {
    private boolean doubleRacket;
    private boolean halfRacket;

    // Getters y setters para doubleRacket
    public boolean isDoubleRacket() {
        return doubleRacket;
    }

    public void setDoubleRacket(boolean doubleRacket) {
        this.doubleRacket = doubleRacket;
    }

    // Getters y setters para halfRacket
    public boolean isHalfRacket() {
        return halfRacket;
    }

    public void setHalfRacket(boolean halfRacket) {
        this.halfRacket = halfRacket;
    }
}

